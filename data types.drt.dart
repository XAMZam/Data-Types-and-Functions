void main() {
  // int data type
  int age = 19;
  print('Age: $age');

  // double data type
  double height = 4.2;
  print('Height: $height');

  // String data type
  String name = 'Samiya Ibrahim';
  print('Name: $name');

  // List data type
  List<String> fruits = ['Watermelon', 'Passion', 'Lemon'];
  print('Fruits: $fruits');

  // Map data type
  Map<String, int> scores = {'Chemistry': 60, 'Geography': 85, 'Kiswahili': 70};
  print('Scores: $scores');
}
